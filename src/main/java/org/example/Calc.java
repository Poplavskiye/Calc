package org.example;

public class Calc {

    public int summ(int a, int b) {

        int summ = a + b;
        System.out.println("Сумма ввода = " + summ);
        return summ;
    }
    public int subt(int a, int b) {

        int subt = a - b;
        System.out.println("Сумма ввода = " + subt );
        return subt;
    }
}
